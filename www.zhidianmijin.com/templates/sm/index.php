五格计算方法： 

1、天格：复姓，合计姓氏之笔画；单姓，再加假添一数。如司马光，司马是复姓，天格是5+10=15；李刚，李是单姓，天格是7+1=8。天格乃祖先留下来的，其数理对人影响不大。 

2、人格，又称"主运"，是整个姓名的中心点，人一生的命运，均由此人格推断。其构成是姓氏最下字与名字最上字笔画数之和。如：刘江，人格数是15+7=22。司马懿，人格数是10+22=32。 

